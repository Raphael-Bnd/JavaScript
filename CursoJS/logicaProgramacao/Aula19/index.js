/*
> maior que
>= maior ou igual que
< menor que
<= menor ou igual que
== igualdade (valor)
=== igualdade estrita(valor e tipo)
!= diferente (valor)
!== diferente estrito (valor e tipo)
*/
const compMaior = 10 > 5;
const compMaiorIgual = 10 >= 5;
console.log(compMaior);
console.log(compMaiorIgual);

const compMenor = 10 < 5;
const compMenorIgual = 10 <= 5;
console.log(compMenor);
console.log(compMenorIgual);

